'''
Write a program which uses a while loop to display a conversion table, with kilograms on the left and pounds on the right. Pounds are rounded to 1 decimal digit.

1 kg = 2.2 lb

Include every other (odd) values of kilograms, from 1 to 199.

Kilograms    Pounds
1                      2.2
3                      6.6
....                   ......
197                 433.4
199                 437.8

'''

print("Kilograms    Pounds")
for kilograms in range(1,201,2):
    pounds = kilograms*2.2
    print("%3i" %kilograms, "         %.2f" %pounds)


